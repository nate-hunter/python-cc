places = ["New Zealand", "South Africa", "Italy", "Scotland", "Norway"]

print('Original:', places)
print('Sorted:', sorted(places))
print('Original:', places)
print('Sorted Reversed:', sorted(places, reverse=True))
print('Original:', places)
places.reverse()
print('Reversed Original:', places)
places.reverse()
print('Reversed Back To Original:', places)
places.sort() 
print('Alphabetical:', places)
places.sort(reverse=True) 
print('Reversed Alphabetical:', places)
